    public void Update(){
        int Bal;
        try {
            String sql = "Select Wallet_Bal from passenger where UserID ="+ UserID;
            ps =connect.prepareStatement(sql);
            rs = ps.executeQuery();
            rs.next();
            Bal=rs.getInt("Wallet_Bal");
            label_money.setText(String.valueOf(Bal));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try {
                rs.close();
                ps.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
    }


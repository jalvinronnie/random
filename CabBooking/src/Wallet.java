    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int Bal;
        try {
            int add=Integer.parseInt(text_amt.getText());
            if(add<=0){
                JOptionPane.showMessageDialog(null,"Enter valid Amount to Deposit");
            }
            else{
                try {
                    String sql = "Select Wallet_Bal from passenger where UserID ="+ UserID;
                    ps =connect.prepareStatement(sql);
                    rs = ps.executeQuery();
                    rs.next();
                    Bal=rs.getInt("Wallet_Bal");
                    Bal+=add;
                    String sql1 = "UPDATE `passenger` SET `Wallet_Bal`="+Bal+" WHERE UserID="+UserID;
                    ps = connect.prepareStatement(sql1);
                    ps.execute();
                    JOptionPane.showMessageDialog(null, "Added Sucessfully");
                    text_amt.setText("");
                    Update();
                } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                }finally{
                        try {
                    rs.close();
                    ps.close();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

